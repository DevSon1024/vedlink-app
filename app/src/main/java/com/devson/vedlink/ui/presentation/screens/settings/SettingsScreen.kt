@OptIn(ExperimentalMaterial3Api::class)
@Composable
fun SettingsScreen(
    viewModel: SettingsViewModel = hiltViewModel()
) {
    val uiState by viewModel.uiState.collectAsState()

    Scaffold(
        topBar = {
            TopAppBar(
                title = {
                    Text(
                        text = "Settings",
                        style = MaterialTheme.typography.titleLarge
                    )
                },
                colors = TopAppBarDefaults.topAppBarColors(
                    containerColor = MaterialTheme.colorScheme.surface
                )
            )
        }
    ) { paddingValues ->
        Column(
            modifier = Modifier
                .fillMaxSize()
                .padding(paddingValues)
                .verticalScroll(rememberScrollState())
                .padding(bottom = 80.dp)
        ) {
            Spacer(modifier = Modifier.height(8.dp))

            // App Info Card
            Card(
                modifier = Modifier
                    .fillMaxWidth()
                    .padding(horizontal = 16.dp, vertical = 8.dp),
                colors = CardDefaults.cardColors(
                    containerColor = MaterialTheme.colorScheme.primaryContainer
                ),
                shape = RoundedCornerShape(16.dp)
            ) {
                Column(
                    modifier = Modifier
                        .fillMaxWidth()
                        .padding(20.dp),
                    horizontalAlignment = Alignment.CenterHorizontally
                ) {
                    Icon(
                        imageVector = Icons.Default.Link,
                        contentDescription = null,
                        modifier = Modifier.size(64.dp),
                        tint = MaterialTheme.colorScheme.primary
                    )
                    Spacer(modifier = Modifier.height(12.dp))
                    Text(
                        text = "VedLink",
                        style = MaterialTheme.typography.headlineMedium,
                        color = MaterialTheme.colorScheme.onPrimaryContainer
                    )
                    Text(
                        text = "Link Manager",
                        style = MaterialTheme.typography.bodyMedium,
                        color = MaterialTheme.colorScheme.onPrimaryContainer.copy(alpha = 0.7f)
                    )
                }
            }

            // Appearance Section
            SettingsSection(title = "Appearance")

            SettingsSwitchItem(
                icon = Icons.Default.DarkMode,
                title = "Dark Mode",
                subtitle = "Use dark theme",
                checked = uiState.isDarkMode,
                onCheckedChange = { viewModel.toggleDarkMode() }
            )

            // Storage Section
            SettingsSection(title = "Storage")

            SettingsItem(
                icon = Icons.Default.Folder,
                title = "Total Links",
                subtitle = "${uiState.totalLinks} links saved",
                onClick = {}
            )

            // Data Management Section
            SettingsSection(title = "Data Management")

            SettingsSwitchItem(
                icon = Icons.Default.Cloud,
                title = "Auto Fetch Metadata",
                subtitle = "Automatically fetch link previews",
                checked = uiState.autoFetchMetadata,
                onCheckedChange = { viewModel.toggleAutoFetchMetadata() }
            )

            SettingsItem(
                icon = Icons.Default.FileDownload,
                title = "Export Data",
                subtitle = "Export all links to JSON",
                onClick = { /* TODO: Implement export */ }
            )

            SettingsItem(
                icon = Icons.Default.FileUpload,
                title = "Import Data",
                subtitle = "Import links from file",
                onClick = { /* TODO: Implement import */ }
            )

            SettingsItem(
                icon = Icons.Default.DeleteSweep,
                title = "Clear All Data",
                subtitle = "Delete all saved links",
                onClick = { /* TODO: Implement clear all */ },
                tint = MaterialTheme.colorScheme.error
            )

            // About Section
            SettingsSection(title = "About")

            SettingsItem(
                icon = Icons.Default.Info,
                title = "Version",
                subtitle = "1.0.0",
                onClick = {}
            )

            SettingsItem(
                icon = Icons.Default.Code,
                title = "Open Source",
                subtitle = "View on GitHub",
                onClick = { /* TODO: Open GitHub */ }
            )

            Spacer(modifier = Modifier.height(16.dp))
        }
